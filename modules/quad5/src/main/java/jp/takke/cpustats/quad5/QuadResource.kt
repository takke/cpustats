   * CPU 使用率を「レベル」に変換する
   *
   * @param cpuUsage CPU使用率[0,100]
   * @return レベル値[0, 5]
   */
  private fun cpuUsageToLevel5(cpuUsage: Int): Int {
    return if (cpuUsage < 5) {
      0
    } else if (cpuUsage < 20) {
      1
    } else if (cpuUsage < 40) {
      2
    } else if (cpuUsage < 60) {
      3
    } else if (cpuUsage < 80) {
      4
    } else {
      5
    }
  }

